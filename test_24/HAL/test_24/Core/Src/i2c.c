//初始化PCF8574
//返回值：0-成功 其他-失败
uint8_t PCF8574_Init(void)
{
	uint8_t temp=0;
	temp = HAL_I2C_IsDeviceReady(&hi2c2,PCF8574_ADDR, 10, HAL_MAX_DELAY); //检查PCF8574是否在位
	PCF8574_WriteOneByte(0xff);
	return temp;
}

//读取PCF8574的8位IO值
uint8_t PCF8574_ReadOneByte(void)
{
	uint8_t temp=0;
	//HAL_I2C_Mem_Read(&hi2c2, PCF8574_ADDR|0x01, 255, I2C_MEMADD_SIZE_8BIT, &temp, 1, 0xff);
	HAL_I2C_Master_Receive(&hi2c2, PCF8574_ADDR|0x01, &temp, 1, 1000);
	return temp;
}

//向PCF8574写入8位IO值
void PCF8574_WriteOneByte(uint8_t DataToWrite)
{
	//HAL_I2C_Mem_Write(&hi2c2, PCF8574_ADDR|0x00, 255, I2C_MEMADD_SIZE_8BIT, &DataToWrite, 1, 0xff);
	HAL_I2C_Master_Transmit(&hi2c2, PCF8574_ADDR|0x00, &DataToWrite, 1, 1000);
}

//设置PCF8574某个IO的高低电平
void PCF8574_WriteBit(uint8_t bit, uint8_t sta)
{
	uint8_t data;
	data=PCF8574_ReadOneByte();
	if(sta==0) data&=~(1<<bit);
	else data|=1<<bit;
	PCF8574_WriteOneByte(data);
}

//读取PCF8574的某个IO值
uint8_t PCF8574_ReadBit(uint8_t bit)
{
	uint8_t data;
	data=PCF8574_ReadOneByte();
	if(data&(1<<bit)) return 1;
	else return 0;
}
